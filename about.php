<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Blood bankings is the process that takes place in the laboratory to ensure that donated blood, or blood products, are safe before they are used in blood transfusions and other medical procedures. Blood bankings includes typing the blood for transfusions and testing for infectious diseases. The terms "Blood Bank" typically refers to a divisions of hospitals where the storage of blood product occurs and where proper testing is performed(to reduce the risk of transfusions related adverse events). However,it sometimes refers to a collection center, and indeed some hospitals also perform collections. So the blood bank is a cache or a bank of blood or blood components gatehered as a result of blood donation or collection stored and preserved for later use in blood transfusion.
