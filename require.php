<h1 align="center">Requirements for donating blood</h1>
<h3>Before you finalize any of the donors, run each donor through "the vital 10" checklist.</h3>
<ul>
  <li class="leaf">The donor should not have donated blood in the last 3 months.</li>
 <li class="leaf">The donor should not have undergone any major surgery within 3 months.</li>
 <li class="leaf">The donor has not had alcohol 48 hours before the donation.</li>
 <li class="leaf">The donor should avoid smoking on the day of donation and 3 hours after donation.</li>
 <li class="leaf">The donor should not have fever at the time of donation.</li>
 <li class="leaf">The donor should have had a good meal before donation.</li>
 <li class="leaf">The donor should disclose whether he is under any medication for malaria, jaundice, etc.</li>
 <li class="leaf">The donor should not have had any tooth extraction in the past 2 weeks.</li>
 <li class="leaf">The donor should not be pregnant.</li>
 <li class="leaf">The donor should not have had any immunization at least one month before.</li>
</ul>
<h3>Please be courteous to donors who are unable to donate.</h3> 
<h3>Please do not force or convince a donor if he/she is not in a position to donate.</h3>

